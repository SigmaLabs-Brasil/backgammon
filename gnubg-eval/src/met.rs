/// Match score from the player-to-move's perspective.
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct MatchState {
    /// Points the player needs to win the match (1..=25).
    pub player_away: i32,
    /// Points the opponent needs to win the match (1..=25).
    pub opponent_away: i32,
    /// True for the Crawford game: cube frozen, one side is 1-away.
    pub crawford: bool,
    /// True for post-Crawford games: cube live again, one side is 1-away.
    pub post_crawford: bool,
}

impl MatchState {
    /// Create and validate a match state.
    ///
    /// # Panics
    ///
    /// Panics when scores are outside 1..=25, Crawford flags conflict, or a
    /// Crawford/post-Crawford flag is used when neither player is 1-away.
    pub fn new(player_away: i32, opponent_away: i32, crawford: bool, post_crawford: bool) -> Self {
        if !(1..=MAX_AWAY).contains(&player_away) {
            panic!("player_away must be in 1..=25");
        }
        if !(1..=MAX_AWAY).contains(&opponent_away) {
            panic!("opponent_away must be in 1..=25");
        }
        if crawford && post_crawford {
            panic!("crawford and post_crawford are mutually exclusive");
        }
        let has_one_away = player_away == 1 || opponent_away == 1;
        if crawford && !has_one_away {
            panic!("crawford only valid when one player is 1-away");
        }
        if post_crawford && !has_one_away {
            panic!("post_crawford only valid when one player is 1-away");
        }

        Self {
            player_away,
            opponent_away,
            crawford,
            post_crawford,
        }
    }

    /// A concrete `MatchState` is never a money game; money games are represented
    /// by `None` match context at call sites.
    pub const fn is_money(&self) -> bool {
        false
    }
}

/// Look up match-winning chance (MWC) in the Rockwell-Kazaross XG MET.
pub fn mwc(state: &MatchState) -> f32 {
    let (row, col) = table_indices(state);
    KAZAROSS_XG_MET[row][col]
}

/// Return the match-winning chance after a single game worth `cubeless_points`.
/// Positive points are won by the player; negative points are won by the opponent.
pub fn mwc_after(state: &MatchState, cubeless_points: i32) -> f32 {
    if cubeless_points == 0 {
        return mwc(state);
    }

    let mut player_away = state.player_away;
    let mut opponent_away = state.opponent_away;
    if cubeless_points > 0 {
        player_away -= cubeless_points;
    } else {
        opponent_away += cubeless_points;
    }

    if player_away <= 0 {
        return 1.0;
    }
    if opponent_away <= 0 {
        return 0.0;
    }

    let one_away = player_away == 1 || opponent_away == 1;
    let post_crawford = one_away && (state.crawford || state.post_crawford);
    let crawford = one_away && !post_crawford;

    mwc(&MatchState::new(
        player_away,
        opponent_away,
        crawford,
        post_crawford,
    ))
}

/// Convert point equity into a linearized MWC swing around the current score.
pub fn match_equity_swing(state: &MatchState, point_equity: f32) -> f32 {
    let current = mwc(state);
    let one_point_swing = (mwc_after(state, 1) - mwc_after(state, -1)) / 2.0;
    let unclamped = current + point_equity * one_point_swing;
    unclamped.clamp(0.0, 1.0) - current
}

fn table_indices(state: &MatchState) -> (usize, usize) {
    if state.post_crawford {
        if state.player_away == 1 && state.opponent_away == 1 {
            return (0, 0);
        }
        if state.player_away == 1 {
            return (0, state.opponent_away as usize);
        }
        if state.opponent_away == 1 {
            return (state.player_away as usize, 0);
        }
        panic!("post_crawford only valid when one player is 1-away");
    }

    (state.player_away as usize, state.opponent_away as usize)
}
