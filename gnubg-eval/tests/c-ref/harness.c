// C reference for gnubg NN evaluation — standalone forward pass
// Matches neuralnet.c Evaluate() exactly

#include <stdio.h>
#include <math.h>
#include <stdlib.h>
#include <string.h>

#define cHidden 128
#define cInput 250
#define cOutput 5

float sigmoid(float x) {
    // Matches gnubg/lib/neuralnet.c: decreasing logistic 1 / (1 + exp(x)).
    return 1.0f / (1.0f + expf(x));
}

int main() {
    // Load the first network from the weight file
    FILE *f = fopen("../../../gnubg-sys/vendor/gnubg.weights", "r");
    if (!f) { perror("fopen"); return 1; }

    char buf[256];
    fgets(buf, sizeof(buf), f); // "GNU Backgammon 1.01"
    
    unsigned int ci, ch, co, nt;
    float bh, bo;
    fscanf(f, "%u %u %u %u %f %f\n", &ci, &ch, &co, &nt, &bh, &bo);
    printf("Network: %ux%ux%u, beta_h=%f, beta_o=%f\n", ci, ch, co, bh, bo);

    // Allocate and read weights
    float *hidden_weights = malloc(ci * ch * sizeof(float));
    for (unsigned int i = 0; i < ci * ch; i++)
        fscanf(f, "%f\n", &hidden_weights[i]);

    float *output_weights = malloc(ch * co * sizeof(float));
    for (unsigned int i = 0; i < ch * co; i++)
        fscanf(f, "%f\n", &output_weights[i]);

    float *hidden_thresholds = malloc(ch * sizeof(float));
    for (unsigned int i = 0; i < ch; i++)
        fscanf(f, "%f\n", &hidden_thresholds[i]);

    float *output_thresholds = malloc(co * sizeof(float));
    for (unsigned int i = 0; i < co; i++)
        fscanf(f, "%f\n", &output_thresholds[i]);

    fclose(f);

    // All-zero input test (like our Rust zero test)
    float *arInput = calloc(ci, sizeof(float));

    // Evaluate: exactly as neuralnet.c
    float *ar = malloc(ch * sizeof(float));
    for (unsigned int i = 0; i < ch; i++)
        ar[i] = hidden_thresholds[i];

    float *prWeight = hidden_weights;
    for (unsigned int i = 0; i < ci; i++) {
        float ari = arInput[i];
        if (ari == 0.0f) {
            prWeight += ch;
        } else {
            float *pr = ar;
            if (ari == 1.0f) {
                for (unsigned int j = ch; j; j--)
                    *pr++ += *prWeight++;
            } else {
                for (unsigned int j = ch; j; j--)
                    *pr++ += *prWeight++ * ari;
            }
        }
    }

    for (unsigned int i = 0; i < ch; i++)
        ar[i] = sigmoid(-bh * ar[i]);

    // Compute hidden stats
    float hmin = ar[0], hmax = ar[0], hsum = 0;
    for (unsigned int i = 0; i < ch; i++) {
        hmin = fmin(hmin, ar[i]);
        hmax = fmax(hmax, ar[i]);
        hsum += ar[i];
    }
    printf("Zero inputs: hidden min=%f max=%f mean=%f\n", hmin, hmax, hsum/ch);
    printf("First 8 hidden: ");
    for (int i = 0; i < 8; i++) printf("%f ", ar[i]);
    printf("\n");

    // Output layer
    prWeight = output_weights;
    float arOutput[5];
    for (unsigned int i = 0; i < co; i++) {
        float r = output_thresholds[i];
        for (unsigned int j = 0; j < ch; j++)
            r += ar[j] * *prWeight++;
        arOutput[i] = sigmoid(-bo * r);
        if (arOutput[i] < 0) arOutput[i] = 0;
        if (arOutput[i] > 1) arOutput[i] = 1;
    }
    printf("Zero inputs output: [%f, %f, %f, %f, %f]\n",
        arOutput[0], arOutput[1], arOutput[2], arOutput[3], arOutput[4]);

    // Now simulate what our Rust inputs look like
    // Read inputs from a file generated by Rust
    // For now, just check with the known 58 non-zero pattern
    // This is a manual simulation of the opening position inputs
    
    // Actually let's just check: what if the input array matches
    // what Rust generates? We'll read from a file.
    FILE *in = fopen("/tmp/rust_inputs.bin", "rb");
    if (in) {
        fread(arInput, sizeof(float), ci, in);
        fclose(in);
        
        // Re-run evaluation with these inputs
        for (unsigned int i = 0; i < ch; i++)
            ar[i] = hidden_thresholds[i];

        prWeight = hidden_weights;
        for (unsigned int i = 0; i < ci; i++) {
            float ari = arInput[i];
            if (ari == 0.0f) {
                prWeight += ch;
            } else {
                float *pr = ar;
                if (ari == 1.0f) {
                    for (unsigned int j = ch; j; j--)
                        *pr++ += *prWeight++;
                } else {
                    for (unsigned int j = ch; j; j--)
                        *pr++ += *prWeight++ * ari;
                }
            }
        }

        for (unsigned int i = 0; i < ch; i++)
            ar[i] = sigmoid(-bh * ar[i]);

        hmin = ar[0]; hmax = ar[0]; hsum = 0;
        for (unsigned int i = 0; i < ch; i++) {
            hmin = fmin(hmin, ar[i]);
            hmax = fmax(hmax, ar[i]);
            hsum += ar[i];
        }
        printf("With Rust inputs: hidden min=%f max=%f mean=%f\n", hmin, hmax, hsum/ch);

        prWeight = output_weights;
        for (unsigned int i = 0; i < co; i++) {
            float r = output_thresholds[i];
            for (unsigned int j = 0; j < ch; j++)
                r += ar[j] * *prWeight++;
            arOutput[i] = sigmoid(-bo * r);
            if (arOutput[i] < 0) arOutput[i] = 0;
            if (arOutput[i] > 1) arOutput[i] = 1;
        }
        printf("With Rust inputs output: [%f, %f, %f, %f, %f]\n",
            arOutput[0], arOutput[1], arOutput[2], arOutput[3], arOutput[4]);
    } else {
        printf("No /tmp/rust_inputs.bin — run Rust test first to generate inputs\n");
    }

    free(ar);
    free(arInput);
    free(hidden_weights);
    free(output_weights);
    free(hidden_thresholds);
    free(output_thresholds);
    return 0;
}
